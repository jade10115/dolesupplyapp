    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'first_name' => 'required|string|max:100', // Validation rule for item_name
            'middle_name' => 'required|string|max:100',  // Validation rule for category
            'last_name' => 'required|string|max:100',             // Validation rule for price
            'ext_name' => 'nullable|string',        // Validation rule for description
            'designation' => 'required|string',               // Validation rule for qty
            'assigned_at' => 'required|string', // Validation rule for unit_of_measurement
        ]);
    
        $product = employees::create($request->all());
        return response()->json($product, 201);
    }

